n1 = int(input('primeiro número: '))
n2 = int(input('segundi número: '))
som = n1+n2
sub = n1-n2
div = n1/n2
divi = n1//n2
sob = n1%n2
pot = n1**n2
print('A soma de {} e {} é {}'.format(n1,n2,som))
print('A subtração de {} e {} é {}'.format(n1,n2,sub))
print('A divisão de {} e {} é {}, e a divisão inteira é {}, sobrando {}'.format(n1,n2,div,divi,sob))
print('E a potência de {} e {} é {}'.format(n1,n2,pot))